                    <h2 class="section-heading">about</h2>
                    <h3>Professionally...</h3>
                    <p class="lead">In my professional life, I most recently served More Than Rewards' mission primarily as a mobile developer. In the past I've been a webmaster, support professional, photographer, multimedia journalist, automotive factory worker, even a dairy farmer. I'm oddly familiar with many things in many industries.</p> 

                    <p>I was in art school when the web went prime time.</p>

                    <p>I wanted to make web things, so I treated a programmer friend of mine to dinner every Sunday and Wednesday while I picked his brain for a few hours on what the web and programming were.</p> 

                    <p>Through these conversations, we were inadvertently training me into an intern position at Rockstar Design, where this friend worked. They hired me. At Rockstar I created my first media using the then Macromedia's Lingo, a VB-like media programming language for a Rich media package called Director. I learned the C programming language, Lingo, Javascript, and Actionscript while there.</p> 

                    <p>To this day – 14 years later – I still thank Kevin for those conversations whenever I see him. Those hours of his time teaching me have changed my life and I pay it forward teaching others whenever I find the chance.</p> 

                    <p>My biggest project I had major contributions to while at Rockstar was the programmatic elements of the Aurora Healthcare Nurse Recruitment CD-ROM. 
                    It was supremely gratifying working on my first enterprise grade media. I love the creative process when applied to coding and technology.</p> 
                    <h3>Open Source...</h3>
                    <p>I graduated from art school with a BFA in 2004. Around the time that I graduated I tripped across PHP and the open-source paradigm.</p> 

                    <p>I was hooked. I was so enamored by the social utility of the open-source process that it made my head spin.</p> 

                    <p>I wanted to contribute, so I created my first icon set for KDE in 2003. I got a job in support that offered free training in virtually any technology and took over 400 credit hours of training in technology ranging from programming languages, encryption, server technologies, and applications. I took the job mostly for this training benefit, alone.</p> 

                    <p>I also volunteered as English documentation and support for an open-source server package called saintWAMP. It was there that I met the lead programmer of the venerable WinAmp Media player and received mentoring from him for the next few years in C, PHP, and Apache web server.</p> 

                    <p>Over the course of the last 10 years I have been involved in open-source software continually, and have been involved in more than 60 projects as of the time of this writing.</p>

                    <h3>In my personal life...</h3>
                    <p>I'm also an internationally published photojournalist/photographer specializing in Creative Commons media for niche publications such as Marxist.com,TreeHugger.com, DeliberateLife Magazine and hundreds of others. I have taken assignments through Demotix.com as a local photojournalist as well as through the Center for Media Change and American Public Media.</p>
